package ch.fhnw.imvs.kanban.config;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.context.MessageSource;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.support.ReloadableResourceBundleMessageSource;
import org.springframework.web.servlet.config.annotation.EnableWebMvc;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

import java.util.Locale;

@Configuration
@EnableWebMvc
public class SpringLocaleConfig extends WebMvcConfigurerAdapter {

    private static Logger log = LoggerFactory.getLogger(SpringLocaleConfig.class);

    @Bean
    public MessageSource messageSource() {
        log.debug("Setting message source for localization.");
        ReloadableResourceBundleMessageSource messageSource = new ReloadableResourceBundleMessageSource();
        messageSource.setBasename("i18n/messages");
        messageSource.setDefaultEncoding("UTF-8");
        messageSource.getMessage("mail.hello", null, new Locale("DE"));
        return messageSource;
    }

}
